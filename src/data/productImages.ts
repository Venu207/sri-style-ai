// Global index tracker to ensure no two products across categories share the same image URL
const usedImages = new Set<string>();

export function getProductImage(categoryId: string, index: number): string {
  const images = categoryImages[categoryId];
  if (!images || images.length === 0) {
    return `https://images.unsplash.com/photo-1558618666-fcd25c85f82e?w=400&h=500&fit=crop&sig=${categoryId}-${index}`;
  }
  
  // Use direct index if available and unique
  if (index < images.length) {
    const img = images[index];
    if (!usedImages.has(img)) {
      usedImages.add(img);
      return img;
    }
  }
  
  // Find any unused image in this category
  for (const img of images) {
    if (!usedImages.has(img)) {
      usedImages.add(img);
      return img;
    }
  }
  
  // Fallback: generate a unique URL using product-specific signature
  const fallback = `https://images.unsplash.com/photo-1558618666-fcd25c85f82e?w=400&h=500&fit=crop&sig=${categoryId}-${index}`;
  return fallback;
}
